class ASCIIArt:
    def __init__(self, ascii_art: str):
        self.image = ascii_art.split('\n')
        if len(self.image) > 1:
            if len(self.image[0]) == 0:
                self.image = self.image[1:]
        if len(self.image) > 1:
            if len(self.image[-1]) == 0:
                self.image = self.image[:-1]
        self.height = len(self.image) + 2
        self.width = max(len(line) for line in self.image) + 5


